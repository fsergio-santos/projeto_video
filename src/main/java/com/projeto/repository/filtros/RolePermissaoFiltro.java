package com.projeto.repository.filtros;

public class RolePermissaoFiltro {
	
	private String roleNome;
	private String permissaoNome;
	private String escopoNome;
	
	public String getRoleNome() {
		return roleNome;
	}
	public void setRoleNome(String roleNome) {
		this.roleNome = roleNome;
	}
	public String getPermissaoNome() {
		return permissaoNome;
	}
	public void setPermissaoNome(String permissaoNome) {
		this.permissaoNome = permissaoNome;
	}
	public String getEscopoNome() {
		return escopoNome;
	}
	public void setEscopoNome(String escopoNome) {
		this.escopoNome = escopoNome;
	}
	
	
	
}
